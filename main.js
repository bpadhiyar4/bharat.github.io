function showNav() {
    document.getElementById("mob-navigation").style.display = "block";
    document.getElementById("myToggle").style.display = "none";
    document.getElementById("myToggleEnd").style.display = "block";
}

function hideNav() {
    document.getElementById("mob-navigation").style.display = "none";
    document.getElementById("myToggleEnd").style.display = "none";
    document.getElementById("myToggle").style.display = "block";
}